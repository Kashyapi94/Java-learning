class Star

{

public static void main(String[] args)

{

 

for(int a=0;a<=5;a++)

{

for(int b=1;b<=a;b++)

{

System.out.print("* ");

}  

  System.out.println("*");

}

for(int c=5;c>=0;c--)

{

for(int d=1;d<=c;d++)

{

System.out.print("* ");

}

System.out.println("*");

}

} 

} 